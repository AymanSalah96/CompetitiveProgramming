ll n, x, y, c;

bool valid(ll t) {
	ll left = max(1LL, y - t);
	ll right = min(n, y + t);
	ll cnt = 0;
	for (ll i = left; i <= right; i++) {
		ll tmp = t - abs(y - i);
		ll up = max(1LL, x - tmp);
		ll down = min(n, x + tmp);
		cnt += down - up + 1;
		if (cnt >= c) return true;
	}
	return false;
}

