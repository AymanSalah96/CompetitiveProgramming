string s;
bool forb[50][50];

int dp[100005][50];

int solve(int idx, int prev) {
	if (idx == sz(s)) return 0;
	int &ret = dp[idx][prev];
	if (ret != -1) return ret;
	int cur = s[idx] - 'a';
	ret = solve(idx + 1, prev) + 1;

	if (!forb[prev][cur])
		ret = min(ret, solve(idx + 1, cur));
	
	return ret;
}

