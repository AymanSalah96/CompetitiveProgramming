int n;
int arr[100005];

int idx(vector<int> &a, int l, int r, int val) {
	while (r - l > 1) {
		int mid = l + (r - l) / 2;
		if (a[mid] >= val) r = mid;
		else l = mid;
	}
	return r;
}

int LIS() {
	vector<int> v(n, 0);
	int ret = 1;
	v[0] = arr[0];
	for (int i = 1; i < n; i++) {
		if (arr[i] < v[0])
			v[0] = arr[i];
		else if (arr[i] > v[ret - 1])
			v[ret++] = arr[i];
		else
			v[idx(v, -1, ret - 1, arr[i])] = arr[i];
	}

	return ret;
}

