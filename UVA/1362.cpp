const int MOD = 1e9;
ll dp[305][305];

string s;

ll solve(int l, int r) {
	if (l == r) return 1;
	ll &ret = dp[l][r];
	if (ret != -1) return ret;
	ret = 0;
	if (s[l] != s[r]) return 0;
	for (int k = l + 2; k <= r; k++)
		if (s[l] == s[k])
			ret = (ret + solve(l + 1, k - 1) * solve(k, r)) % MOD;
	return ret;
}

